source /root/func.sh

lock="biglock"
locked=$(getLockFile $lock)
if [ "$locked" != "1" ]
then
  exit 1
fi

usage() {
  printf "Usage:\n %s (basename $0), return list of eth device" >&2
}

ethlist=""
for i in `ls -1 /sys/class/net/ | grep eth`
do
  ethlist=$ethlist","$i
done
echo ${ethlist:1}

unlock_exit 1 $lock $locked
